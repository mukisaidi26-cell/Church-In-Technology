package org.example;

public class Incrementing {
    public static void main(String[] args) {
        //Incrementing
        int x = 1;
        ++x;
        System.out.println(x);

        //Decrementing
        --x;
        System.out.println(x);

        //Simple program counting people
        int peopleInRoom = 0;

        //3 people enter the room
        peopleInRoom++;
        peopleInRoom++;
    }
}
