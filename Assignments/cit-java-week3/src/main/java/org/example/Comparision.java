package org.example;

public class Comparision {
    public static void main(String[] args) {
        int x = 5;
        int y = 8;
        //Making comparisons
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y);
        System.out.println(x >= y);
        System.out.println(x == y);
        System.out.println(x != y);

        //Logical Operators
        System.out.println(x<y && x<=y);
    }
}
